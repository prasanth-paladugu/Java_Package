//DEMO CLASS FOR TESTING CLASS ON PACKAGE CREATION
package com.red;							//CREATING RED SUBPACKAGE IN COM PACKAGE
public class DEMO
{
	private void m1()						//PRIVATE METHOD
	{
		System.out.println("PRIVATE");
	}
	void m2()								//DEFAULT METHOD
	{
		System.out.println("DEFAULT");
	}
	protected void m3()						//PROTECTED METHOD
	{
		System.out.println("PROTECTED");
	}
	public void m4()						//PUBLIC METHOD
	{
		System.out.println("PUBLIC");
	}
}